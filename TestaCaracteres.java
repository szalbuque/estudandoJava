public class TestaCaracteres {
    public static void main(String[] args){
        char letra = 'a';
        System.out.println(letra);
        // String não é um tipo primitivo
        String frase = "esta é uma frase";
        System.out.println(frase);
        // Ele converte e concatena
        frase = frase + 333;
        System.out.println(frase);
    }
}
